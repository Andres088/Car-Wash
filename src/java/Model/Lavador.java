
package Model;


public class Lavador {
    
    private int codlav;
    private String nom;
    private String hora;
    private Double punt;

    public int getCodlav() {
        return codlav;
    }

    public void setCodlav(int codlav) {
        this.codlav = codlav;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public Double getPunt() {
        return punt;
    }

    public void setPunt(Double punt) {
        this.punt = punt;
    }
    
}
