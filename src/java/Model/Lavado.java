
package Model;


public class Lavado {
    
    private int codserv;
    private String tipolav;
    private String tipopag;
    private String placa;
    private int codlav;
    private int codcar;

    public int getCodserv() {
        return codserv;
    }

    public void setCodserv(int codserv) {
        this.codserv = codserv;
    }

    public String getTipolav() {
        return tipolav;
    }

    public void setTipolav(String tipolav) {
        this.tipolav = tipolav;
    }

    public String getTipopag() {
        return tipopag;
    }

    public void setTipopag(String tipopag) {
        this.tipopag = tipopag;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public int getCodlav() {
        return codlav;
    }

    public void setCodlav(int codlav) {
        this.codlav = codlav;
    }

    public int getCodcar() {
        return codcar;
    }

    public void setCodcar(int codcar) {
        this.codcar = codcar;
    }
    
}
